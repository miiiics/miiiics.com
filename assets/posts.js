function galleryDotOpacity(dist) {
  return [1, 0.6, 0.3, 0.1][dist] || 0;
}

window.GALLERIES = {};
var _gallerySeq = 0;

window.galleryGoto = function (gid, idx) {
  var el = document.getElementById(gid);
  if (!el) return;
  var imgs = window.GALLERIES[gid];
  var total = imgs.length;
  if (idx < 0 || idx >= total) return;
  var currentIdx = parseInt(el.dataset.index);
  if (idx === currentIdx) return;
  var dir = idx > currentIdx ? 1 : -1;
  var wrapper = el.querySelector('.image-wrapper');
  var outImg = wrapper.querySelector('img');

  wrapper.style.height = wrapper.offsetHeight + 'px';
  wrapper.style.position = 'relative';
  wrapper.style.overflow = 'hidden';
  outImg.style.position = 'absolute';
  outImg.style.top = '0';
  outImg.style.left = '0';
  outImg.style.width = '100%';

  var inImg = document.createElement('img');
  inImg.src = imgs[idx].src;
  inImg.alt = imgs[idx].caption || '';
  inImg.style.position = 'absolute';
  inImg.style.top = '0';
  inImg.style.left = '0';
  inImg.style.width = '100%';
  inImg.style.transform = 'translateX(' + (dir * 100) + '%)';
  wrapper.appendChild(inImg);
  inImg.offsetHeight; // force reflow

  var ease = '0.45s cubic-bezier(0.25, 0.46, 0.45, 0.94)';
  outImg.style.transition = 'transform ' + ease;
  inImg.style.transition = 'transform ' + ease;
  outImg.style.transform = 'translateX(' + (-dir * 100) + '%)';
  inImg.style.transform = 'translateX(0)';

  var track = el.querySelector('.gallery-dots-track');
  if (track) track.style.transform = 'translateX(' + ((total - 1) / 2 - idx) * 18 + 'px)';
  var dots = el.querySelectorAll('.gallery-dot');
  dots.forEach(function (d, i) {
    d.classList.toggle('gallery-dot-active', i === idx);
    d.style.opacity = galleryDotOpacity(Math.abs(i - idx));
  });
  el.querySelector('.gallery-prev').style.display = idx === 0 ? 'none' : '';
  el.querySelector('.gallery-next').style.display = idx === total - 1 ? 'none' : '';

  setTimeout(function () {
    wrapper.removeChild(outImg);
    inImg.style.cssText = '';
    wrapper.style.height = '';
    wrapper.style.position = '';
    wrapper.style.overflow = '';
    el.dataset.index = idx;
    var caption = el.querySelector('.image-caption');
    if (caption) {
      caption.textContent = imgs[idx].caption || '';
      caption.style.display = imgs[idx].caption ? '' : 'none';
    }
  }, 450);
};

window.galleryNav = function (gid, dir) {
  var el = document.getElementById(gid);
  if (!el) return;
  window.galleryGoto(gid, parseInt(el.dataset.index) + dir);
};

function renderInline(text) {
  return text
    .replace(/\*_([^*_]+)_\*/g, '<strong><em>$1</em></strong>')
    .replace(/_\*([^*_]+)\*_/g, '<strong><em>$1</em></strong>')
    .replace(/\*([^*]+)\*/g, '<strong>$1</strong>')
    .replace(/_([^_]+)_/g, '<em>$1</em>')
    .replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2">$1</a>');
}

window.renderPosts = function (containerId, pageCat) {
  var posts = pageCat
    ? window.POSTS.filter(function (p) { return p.cats.indexOf(pageCat) !== -1; })
    : window.POSTS;
  var container = document.getElementById(containerId);
  if (!container) return;
  if (!posts.length) {
    container.innerHTML = '<p class="empty-state">Nothing here yet — check back soon.</p>';
    return;
  }
  var html = '';
  posts.forEach(function (p) {
    var badges = p.cats.slice().sort().map(function (c) {
      var d = window.BADGE_DEFS[c];
      return '<a href="' + d.href + '" class="badge ' + d.cls + '">' + d.label + '</a>';
    }).join('');
    var hasBody = p.body && p.body.length;
    html += '<article class="post' + (hasBody ? ' post-has-body' : '') + '"><div class="post-meta">' + badges + '<span>' + p.date + '</span></div>';

    // Title: plain text when no url, linked when url is present
    if (p.url) {
      html += '<h2 class="post-title"><a href="' + p.url + '" target="_blank" rel="noopener">' + p.title + '</a></h2>';
    } else {
      html += '<h2 class="post-title">' + p.title + '</h2>';
    }

    // YouTube embed
    if (p.embedSrc) {
      html += '<div class="video-wrapper"><iframe width="560" height="315" src="' + p.embedSrc + '" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe></div>';
    }

    // Body blocks: paragraphs and inline images
    if (p.body && p.body.length) {
      html += '<div class="post-body">';
      p.body.forEach(function (block) {
        if (block.type === 'p') {
          html += '<p>' + renderInline(block.text) + '</p>';
        } else if (block.type === 'img') {
          html += '<div class="image-wrapper"><img src="' + block.src + '" alt="' + (block.caption || '') + '"></div>';
          if (block.caption) {
            html += '<p class="image-caption">' + block.caption + '</p>';
          }
        } else if (block.type === 'gallery') {
          var gid = 'gallery-' + (++_gallerySeq);
          var imgs = block.images;
          window.GALLERIES[gid] = imgs;
          var first = imgs[0] || {};
          html += '<div class="gallery" id="' + gid + '" data-index="0">';
          html += '<div class="gallery-image-area">';
          html += '<button class="gallery-btn gallery-prev" onclick="galleryNav(\'' + gid + '\',-1)" aria-label="Previous image" style="display:none">&#8249;</button>';
          html += '<div class="image-wrapper"><img src="' + first.src + '" alt="' + (first.caption || '') + '"></div>';
          html += '<button class="gallery-btn gallery-next" onclick="galleryNav(\'' + gid + '\',1)" aria-label="Next image"' + (imgs.length <= 1 ? ' style="display:none"' : '') + '>&#8250;</button>';
          html += '</div>';
          if (imgs.length > 1) {
            var initShift = ((imgs.length - 1) / 2) * 18;
            html += '<div class="gallery-dots"><div class="gallery-dots-track" style="transform:translateX(' + initShift + 'px)">';
            imgs.forEach(function (_, i) {
              html += '<span class="gallery-dot' + (i === 0 ? ' gallery-dot-active' : '') + '" onclick="galleryGoto(\'' + gid + '\',' + i + ')" style="opacity:' + galleryDotOpacity(i) + '"></span>';
            });
            html += '</div></div>';
          }
          if (first.caption) html += '<p class="image-caption">' + first.caption + '</p>';
          html += '</div>';
        } else if (block.type === 'video') {
          html += '<div class="video-wrapper"><iframe width="560" height="315" src="' + block.embedSrc + '" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe></div>';
          if (block.caption) {
            html += '<p class="image-caption">' + block.caption + '</p>';
          }
        }
      });
      html += '</div>';
    }

    html += '</article>';
  });
  container.innerHTML = html;

  container.querySelectorAll('.post-body').forEach(function (body) {
    if (body.scrollHeight > 480) {
      body.classList.add('post-body-collapsed');
      var btn = document.createElement('button');
      btn.className = 'show-more-btn';
      btn.textContent = 'Show More ↓';
      btn.onclick = function () {
        var fullHeight = body.scrollHeight;
        body.style.maxHeight = '320px';
        body.style.overflow = 'hidden';
        body.classList.remove('post-body-collapsed');
        body.offsetHeight; // force reflow so transition fires
        body.style.transition = 'max-height 1.1s ease-in-out';
        body.style.maxHeight = fullHeight + 'px';
        btn.remove();
        setTimeout(function () { body.style.cssText = ''; }, 1110);
      };
      body.parentNode.insertBefore(btn, body.nextSibling);
    }
  });
};
